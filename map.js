const SCREEN_WIDTH  = 1920;
const SCREEN_HEIGHT = 1080;

const OBJECT_W = 40;
const OBJECT_H = 40;

const canvas = document.getElementById("can");
const ctx = canvas.getContext("2d");
canvas.width = SCREEN_WIDTH;
canvas.height = SCREEN_HEIGHT;

class Map{
    constructor(mapn,map_array){
        this.mapn = mapn;
        this.map_array = map_array;
    }

    draw(){
        for(let i=0;i<48*27;i++){
            if(this.map_array[i]==1){
                ctx.fillStyle="blue";
                ctx.fillRect((i % 48) * OBJECT_H, Math.floor(i / 48) * OBJECT_W, OBJECT_H, OBJECT_W);
            }
            else {
                ctx.fillStyle="white";
                ctx.fillRect((i % 48) * OBJECT_H, Math.floor(i / 48) * OBJECT_W, OBJECT_H, OBJECT_W);
            }
        }
    }
}


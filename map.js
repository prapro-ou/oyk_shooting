const OBJECT_W = 40;
const OBJECT_H = 40;

const ROW_N = SCREEN_WIDTH/OBJECT_W;
const COLUMN_N = SCREEN_HEIGHT/OBJECT_H;

const canvas = document.getElementById("can");
const ctx = canvas.getContext("2d");
canvas.width = SCREEN_WIDTH;
canvas.height = SCREEN_HEIGHT;

class Map{
    constructor(mapn,map_array,enemy_array){
        this.mapn = mapn;
        this.map_array = map_array;
    }

    draw(){
        //map描画
        for(let i=0;i<48*27;i++){
            if(this.map_array[i]==1){
                ctx.fillStyle="blue";
                ctx.fillRect((i % ROW_N) * OBJECT_H, Math.floor(i / ROW_N) * OBJECT_W, OBJECT_H, OBJECT_W);
            }
            else {
                ctx.fillStyle="white";
                ctx.fillRect((i % ROW_N) * OBJECT_H, Math.floor(i / ROW_N) * OBJECT_W, OBJECT_H, OBJECT_W);
            }
        }

        //enemy描画
        for(let i=0;i<48*27;i++){
            if(this.map_array[i]==2) new enenmy.draw(((i % ROW_N) * OBJECT_H+OBJECT_H)%2, (Math.floor(i / ROW_N) * OBJECT_W+OBJECT_W)%2); //座標を計算
            else if(this.map_array[i]==3) new enenmy.draw(((i % ROW_N) * OBJECT_H+OBJECT_H)%2, (Math.floor(i / ROW_N) * OBJECT_W+OBJECT_W)%2);
            else if(this.map_array[i]==4) new enenmy.draw(((i % ROW_N) * OBJECT_H+OBJECT_H)%2, (Math.floor(i / ROW_N) * OBJECT_W+OBJECT_W)%2);
        }
    }
}

